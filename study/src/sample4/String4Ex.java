package sample4;

public class String4Ex {
    public static void main(String[] args) {
        int i = 7;
        System.out.println("Java " + i);
        System.out.println("Java " + 7);
        System.out.println(7 + 1 + "Java " + 7 + 1);
        String java = String.format("%s, %d", "Java", 14);
        System.out.println(java);
    }
}
